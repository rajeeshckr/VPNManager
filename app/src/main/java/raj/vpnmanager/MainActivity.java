    public void onClickBtnMonitor(View v)
    {
        if(VPNTimer.getInstance(getApplicationContext()).StartOrExecute()){
            ShowToastMessage("Started monitoring");
        }
        else{
            ShowToastMessage("Failed to monitor");
        }
    }

    public void onClickBtnStopMonitor(View v)
    {
        if(VPNTimer.getInstance(getApplicationContext()).Stop()){
            ShowToastMessage("Stopped monitoring");
        }
        else{
            ShowToastMessage("Failed to stop monitor");
        }
    }

    private void ShowToastMessage(CharSequence message) {
        Context context = getApplicationContext();
        int duration = Toast.LENGTH_LONG;
        Toast toast = Toast.makeText(context, message, duration);
        toast.show();
    }

